static bool send_fx(katana_sysex_send_fn send_raw_sysex, void *ctx, const uint8_t msg[KATANA_SYSEX_FX_LEN])
{
    return send_raw_sysex(msg, KATANA_SYSEX_FX_LEN, ctx);
}

esp_err_t katana_send_handshake_sequence(katana_sysex_send_fn send_raw_sysex, void *ctx)
{
    if (!send_raw_sysex) {
        return ESP_ERR_INVALID_ARG;
    }
    send_raw_sysex(katana_sysex_identity_15, sizeof(katana_sysex_identity_15), ctx);
    vTaskDelay(pdMS_TO_TICKS(KATANA_MS3_GAP_MS));
    send_raw_sysex(katana_sysex_identity_15, sizeof(katana_sysex_identity_15), ctx);
    vTaskDelay(pdMS_TO_TICKS(KATANA_MS3_GAP_MS));
    send_raw_sysex(katana_sysex_editor_mode_15, sizeof(katana_sysex_editor_mode_15), ctx);
    vTaskDelay(pdMS_TO_TICKS(KATANA_MS3_GAP_MS));
    send_raw_sysex(katana_sysex_channel[0], KATANA_SYSEX_CH_LEN, ctx);
    vTaskDelay(pdMS_TO_TICKS(100));
    return ESP_OK;
}

uint8_t katana_channel_midi_program(unsigned channel_1_to_4)
{
    if (channel_1_to_4 < 1 || channel_1_to_4 > 4) {
        return 0;
    }
    return katana_midi_program_change[channel_1_to_4 - 1];
}

esp_err_t katana_send_channel(katana_sysex_send_fn send_raw_sysex, void *ctx, unsigned channel_1_to_4)
{
    if (!send_raw_sysex || channel_1_to_4 < 1 || channel_1_to_4 > 4) {
        return ESP_ERR_INVALID_ARG;
    }

    const unsigned idx = channel_1_to_4 - 1;

    if (!send_raw_sysex(katana_sysex_channel[idx], KATANA_SYSEX_CH_LEN, ctx)) {
        return ESP_FAIL;
    }
    vTaskDelay(pdMS_TO_TICKS(KATANA_MS3_GAP_MS));

    return ESP_OK;
}

esp_err_t katana_send_effect_cycle_step(katana_sysex_send_fn send_raw_sysex, void *ctx, katana_effect_t effect,
                                        unsigned step)
{
    if (!send_raw_sysex || effect >= KATANA_EFFECT_COUNT || step >= KATANA_EFFECT_CYCLE_STEPS) {
        return ESP_ERR_INVALID_ARG;
    }

    const katana_effect_sysex_t *fx = &katana_effect_sysex[effect];

    switch (step) {
    case 0:
        send_fx(send_raw_sysex, ctx, fx->on); // Effect on
        vTaskDelay(pdMS_TO_TICKS(20));
        send_fx(send_raw_sysex, ctx, fx->green); // Set effect type to green
        break;
    case 1:
        send_fx(send_raw_sysex, ctx, fx->red); // Set effect type to red
        break;
    case 2:
        send_fx(send_raw_sysex, ctx, fx->yellow); // Set effect type to yellow
        break;
    case 3:
        send_fx(send_raw_sysex, ctx, fx->off); // Turn effect off
        break;
    default:
        return ESP_ERR_INVALID_ARG;
    }

    return ESP_OK;
}
